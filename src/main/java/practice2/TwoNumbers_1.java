public class TwoNumbers_1 {
    public static void main (String[] args){

        // Two numbers
        int q = 21;
        int w = 8;

        // Calculation
        int calc1 = q / w;
        int remainder = q % w;

        // Display result
        System.out.println(q+"/"+ w + " = " + calc1 + " and the remainder is = " + remainder +".");

    }
}
